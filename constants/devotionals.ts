export function getTodayDevotional(viewedIds: string[] = []): Devotional {
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000);
  
  const unviewed = devotionals.filter(d => !viewedIds.includes(d.id));
  
  if (unviewed.length > 0) {
    return unviewed[dayOfYear % unviewed.length];
  }
  
  return devotionals[dayOfYear % devotionals.length];
}

export function getPersonalizedDevotional(viewedIds: string[], preferences: string[] = []): Devotional {
  const unviewed = devotionals.filter(d => !viewedIds.includes(d.id));
  
  if (unviewed.length === 0) {
    return devotionals[Math.floor(Math.random() * devotionals.length)];
  }
  
  if (preferences.length > 0) {
    const preferredDevotionals = unviewed.filter(d => 
      preferences.some(pref => 
        d.title.toLowerCase().includes(pref.toLowerCase()) ||
        d.reflection.toLowerCase().includes(pref.toLowerCase())
      )
    );
    
    if (preferredDevotionals.length > 0) {
      return preferredDevotionals[Math.floor(Math.random() * preferredDevotionals.length)];
    }
  }
  
  return unviewed[Math.floor(Math.random() * unviewed.length)];
}
